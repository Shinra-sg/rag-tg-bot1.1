/**
 * –ü—Ä–æ–≤–µ—Ä—è–µ—Ç –≥–æ—Ç–æ–≤–Ω–æ—Å—Ç—å —Å–∏—Å—Ç–µ–º—ã –∫ —Ä–∞–±–æ—Ç–µ
 */
export async function checkSystemHealth(): Promise<{
  healthy: boolean;
  issues: string[];
  stats: {
    chunks: number;
    documents: number;
    users: number;
  };
}> {
  const issues: string[] = [];
  const stats = {
    chunks: 0,
    documents: 0,
    users: 0
  };

  try {
    // –ü—Ä–æ–≤–µ—Ä—è–µ–º –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫ –ë–î
    await pool.query("SELECT 1");
    
    // –ü—Ä–æ–≤–µ—Ä—è–µ–º –æ—Å–Ω–æ–≤–Ω—ã–µ —Ç–∞–±–ª–∏—Ü—ã
    const tables = ['instruction_chunks', 'documents', 'search_logs'];
    for (const table of tables) {
      try {
        const result = await pool.query(`SELECT COUNT(*) FROM ${table}`);
        
        if (table === 'instruction_chunks') stats.chunks = parseInt(result.rows[0].count);
        if (table === 'documents') stats.documents = parseInt(result.rows[0].count);
      } catch (error) {
        issues.push(`–û—à–∏–±–∫–∞ –¥–æ—Å—Ç—É–ø–∞ –∫ —Ç–∞–±–ª–∏—Ü–µ ${table}: ${error}`);
      }
    }

    // –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π
    try {
      const usersResult = await pool.query(`
        SELECT COUNT(DISTINCT user_id) FROM search_logs
      `);
      stats.users = parseInt(usersResult.rows[0].count);
    } catch (error) {
      // –¢–∞–±–ª–∏—Ü–∞ –º–æ–∂–µ—Ç –Ω–µ —Å—É—â–µ—Å—Ç–≤–æ–≤–∞—Ç—å
      stats.users = 0;
    }

    // –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ –¥–∞–Ω–Ω—ã—Ö
    if (stats.chunks === 0) {
      issues.push("–ù–µ—Ç –¥–∞–Ω–Ω—ã—Ö –≤ —Ç–∞–±–ª–∏—Ü–µ instruction_chunks");
    }
    
    if (stats.documents === 0) {
      issues.push("–ù–µ—Ç –¥–æ–∫—É–º–µ–Ω—Ç–æ–≤ –≤ —Å–∏—Å—Ç–µ–º–µ");
    }

    return {
      healthy: issues.length === 0,
      issues,
      stats
    };

  } catch (error) {
    issues.push(`–û—à–∏–±–∫–∞ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –∫ –±–∞–∑–µ –¥–∞–Ω–Ω—ã—Ö: ${error}`);
    return {
      healthy: false,
      issues,
      stats
    };
  }
}

